/**
 * @file Config.h
 * @brief Объявление структуры Config и класса-менеджера ConfigManager.
 */

#ifndef CONFIG_H
#define CONFIG_H

#include <string>
#include <map>
#include <cstdint>
#include <iosfwd>
#include "Types.h"

/**
 * @struct OperatorInfo
 * @brief Хранит данные об операторе: его тип и арность.
 */
struct OperatorInfo {
    OperatorType type;
    Arity arity;
};

/**
 * @struct Config
 * @brief Структура для хранения параметров конфигурации программы.
 *
 * Содержит как настройки синтаксического анализатора (парсера), 
 * так и параметры окружения (пути к файлам, ограничения ресурсов).
 */
struct Config {
    /** @name Настройки синтаксиса
     * Определяют то, как парсер интерпретирует входное выражение.
     */
    ///@{
    
    /// Карта: "символ оператора" : {Тип, Арность}
    std::map<std::string, OperatorInfo> operators = {
        {"&", {OperatorType::AND, 2}},
        {"|", {OperatorType::OR,  2}},
        {"!", {OperatorType::NOT, 1}}
    };

    /// Префикс, используемый для идентификации вызовов пользовательских функций.
    char userFuncPrefix = '$';
    ///@}

    /** @name Методы-помощники для парсера */
    ///@{
    
    /**
     * @brief Проверяет, является ли переданный токен оператором.
     * @param token Строковое представление токена из выражения.
     * @return true, если токен найден в списке поддерживаемых операторов, иначе false.
     */
    bool isOperator(const std::string& token) const;

    /**
     * @brief Возвращает количество аргументов (арность), требуемое для данного оператора.
     * @param token Строковое представление оператора.
     * @return Количество операндов, которое необходимо извлечь из стека.
     * @throws std::runtime_error Если токен не является оператором.
     */
    Arity getOperatorArity(const std::string& token) const;
    
    ///@}

    /** @name Настройки выполнения
     * Параметры, управляющие вводом/выводом и ограничениями системы.
     */
    ///@{
    
    /// Путь к файлу, в который будет сохранен результат (CSV-таблица истинности).
    /// Пустая строка означает, что флаг -o не был задан.
    std::string outputFilePath = "";
    
    /// Путь к файлу, содержащему определения пользовательских функций.
    std::string functionsFilePath = "functions.txt";
    
    /// Символ-разделитель, используемый при записи данных в CSV-файл.
    char csvDelimiter = ',';

    /// Путь к файлу, содержащему входное логическое выражение в формате ОПН.
    /// Пустая строка означает интерактивный режим ввода из консоли.
    std::string inputFilePath = "";
    ///@}
};

/**
 * @class ConfigManager
 * @brief Менеджер конфигурации.
 *
 * Предоставляет централизованный доступ к настройкам приложения. 
 * Отвечает за инициализацию параметров, парсинг аргументов командной строки 
 * и загрузку внешних файлов конфигурации.
 */
class ConfigManager {
private:
    Config currentConfig; ///< Внутренний объект текущих настроек.

    /**
     * @brief Применяет одну пару "ключ-значение" к текущей конфигурации.
     *
     * Единая интерпретация настроек, используемая как при загрузке из файла (loadFromFile)
     * так и при разборе аргументов командной строки.
     * @param key Имя настройки (например, "output_file").
     * @param value Значение настройки.
     * @throws Error Выбрасывает CONFIG_ERROR при неизвестном ключе или некорректном значении.
     */
    void applySetting(const std::string& key, const std::string& value);

    /**
     * @brief Разбирает содержимое конфигурации из потока.
     *
     * Читает поток построчно, пропускает пустые строки и комментарии (#),
     * разбирает строки формата key=value и применяет их через applySetting.
     * Открытие файла и обработка ошибок ввода-вывода выполняются вызывающей
     * стороной (loadFromFile) - функция работает с уже открытым потоком.
     * @param in Входной поток с содержимым конфигурации.
     * @throws Error Выбрасывает CONFIG_ERROR при неверном формате строки.
     */
    void parseFile(std::istream& in);

public:
    /**
     * @brief Конструктор по умолчанию. Инициализирует настройки значениями по умолчанию.
     */
    ConfigManager();

    /**
     * @brief Парсит аргументы командной строки и обновляет настройки.
     * @param argc Количество аргументов.
     * @param argv Массив строк-аргументов.
     * @throws Error Выбрасывает ошибку при некорректных аргументах.
     */
    void parseCommandLine(int argc, char* argv[]);

    /**
     * @brief Загружает настройки из внешнего файла.
     * @param filePath Путь к конфигурационному файлу.
     * @throws Error Выбрасывает ошибку, если файл отсутствует или имеет неверный формат.
     */
    void loadFromFile(const std::string& filePath);

    /**
     * @brief Возвращает доступ к текущей конфигурации.
     * @return Константная ссылка на структуру Config.
     */
    const Config& getConfig() const;
};

#endif // CONFIG_H