#ifndef USER_FUNCT_H
#define USER_FUNCT_H

#include <string>
#include <map>
#include <cstddef>

// Опережающее объявление: менеджеру достаточно знать, что класс Node существует,
// чтобы работать с указателями на него.
class Node;

/**
 * @brief Структура, описывающая пользовательскую логическую функцию.
 */
struct UserFunction {
    std::string name;           ///< Уникальное имя функции.
    size_t arity = 0;           ///< Количество принимаемых аргументов (арность).
    Node* bodyRoot = nullptr;   ///< Корень AST поддерева, представляющего тело функции.
};

/**
 * @brief Менеджер пользовательских функций.
 * 
 * Отвечает за хранение, проверку сигнатур и автоматическое управление памятью
 * абстрактных синтаксических деревьев (AST) пользовательских функций.
 */
class FunctManager {
private:
    /**
     * @brief Хранилище зарегистрированных функций. 
     * 
     * Используется std::map для обеспечения стабильности адресов элементов: 
     * при добавлении новых функций адреса существующих в памяти не меняются, 
     * что позволяет безопасно хранить указатели на них внутри узлов AST.
     */
    std::map<std::string, UserFunction> functions;

public:
    /**
     * @brief Конструктор по умолчанию.
     */
    FunctManager() = default;

    /**
     * @brief Деструктор. 
     * 
     * Освобождает память всех выделенных поддеревьев (bodyRoot) зарегистрированных функций.
     */
    ~FunctManager();

    // Запрещаем копирование, чтобы избежать двойного удаления указателей (идиома RAII)
    FunctManager(const FunctManager&) = delete;
    FunctManager& operator=(const FunctManager&) = delete;

    /**
     * @brief Регистрирует новую функцию в системе.
     * 
     * Менеджер принимает структуру `func` и берет на себя ответственность за 
     * владение и последующее удаление указателя `bodyRoot`.
     * 
     * @param func Структура с данными функции (имя, арность, корень AST).
     * @throws Error Если функция с именем `func.name` уже зарегистрирована.
     */
    void registerFunction(UserFunction func);

    /**
     * @brief Проверяет наличие функции в менеджере.
     * 
     * @param name Имя функции для проверки.
     * @return true, если функция найдена, иначе false.
     */
    bool hasFunction(const std::string& name) const;

    /**
     * @brief Возвращает стабильный указатель на зарегистрированную функцию.
     * 
     * Метод предназначен для использования на этапе сборки AST (парсинга).
     * 
     * @param name Имя искомой функции.
     * @return Константный указатель на структуру UserFunction.
     * @throws Error Если функция с таким именем не найдена.
     */
    const UserFunction* getFunction(const std::string& name) const;
};

#endif // USER_FUNCT_H